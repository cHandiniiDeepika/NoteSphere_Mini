// Get all unique subjects from all branches
export const getAllSubjects = () => {
  const allSubjects = [];
  Object.values(BTECH_SUBJECTS).forEach(branch => {
    Object.values(branch).forEach(year => {
      Object.values(year).forEach(semester => {
        allSubjects.push(...semester);
      });
    });
  });
  return [...new Set(allSubjects)].sort();
};

// Get subjects for specific branch, year, and semester
export const getSubjectsForBranch = (branch, year, semester) => {
  const branchData = BTECH_SUBJECTS[branch];
  if (!branchData) return [];
  
  const yearData = branchData[year];
  if (!yearData) return [];
  
  return yearData[semester] || [];
};

// Search subjects with autocomplete
export const searchSubjects = (query, branch = null, year = null, semester = null) => {
  const allSubjects = branch && year && semester 
    ? getSubjectsForBranch(branch, year, semester)
    : getAllSubjects();
    
  if (!query.trim()) return allSubjects;
  
  return allSubjects.filter(subject =>
    subject.toLowerCase().includes(query.toLowerCase())
  );
};

// Get all branches
export const getAllBranches = () => {
  return Object.keys(BTECH_SUBJECTS);
};

// Get all years for a branch
export const getYearsForBranch = (branch) => {
  const branchData = BTECH_SUBJECTS[branch];
  return branchData ? Object.keys(branchData) : [];
};

// Get all semesters for a branch and year
export const getSemestersForBranchYear = (branch, year) => {
  const branchData = BTECH_SUBJECTS[branch];
  if (!branchData) return [];
  
  const yearData = branchData[year];
  return yearData ? Object.keys(yearData) : [];
};
